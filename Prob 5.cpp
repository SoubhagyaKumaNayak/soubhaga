#include <iostream>

using namespace std;

int main() {
       int i;
       int n=2520;
       i=11;
       while(i<=20)
      {
       for(i=11;i<=20;i++)
       {
           if(n%i!=0)
           {
               n+=1;
               break;
           }
       }
      }
       cout<<n;
       return 0;
       
} 















  


    

   




